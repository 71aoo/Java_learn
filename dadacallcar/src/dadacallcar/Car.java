package dadacallcar;

public class Car {
	private int number;
	private String name;
	private int rent;
	private int forpp;
	private int forgood;

	public int getRent() {
		return rent;
	}
	public void setRent(int rent) {
		this.rent = rent;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getForpp() {
		return forpp;
	}
	public void setForpp(int forpp) {
		this.forpp = forpp;
	}
	public int getForgood() {
		return forgood;
	}
	public void setForgood(int forgood) {
		this.forgood = forgood;
	}
}
